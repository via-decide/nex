@dataclass
class EpubMetadata:
    """
    Metadata container for the EPUB publication.
    Complies with Dublin Core metadata elements required by EPUB3.
    """
    title: str
    author: str
    language: str = "en"
    identifier: str = field(default_factory=lambda: str(uuid.uuid4()))
    publisher: str = "Nex Autonomous Research Engine"
    description: str = "A comprehensive research report generated by Nex."
    subjects: List[str] = field(default_factory=list)
    publication_date: str = field(
        default_factory=lambda: datetime.datetime.now(datetime.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    )
    rights: str = "All rights reserved."
    cover_image_path: Optional[Union[str, Path]] = None
    isbn: Optional[str] = None
    
    def validate(self) -> None:
        """Validates that all required metadata fields are present and correctly formatted."""
        if not self.title:
            raise MetadataMissingError("EPUB title is required.")
        if not self.author:
            raise MetadataMissingError("EPUB author is required.")
        if not self.identifier:
            raise MetadataMissingError("EPUB identifier (UUID/ISBN) is required.")


@dataclass
class EpubChapter:
    """
    Represents a single chapter or section in the EPUB.
    """
    title: str
    content_markdown: str
    order: int
    html_content: str = ""
    file_name: str = ""
    id: str = ""
    is_frontmatter: bool = False

    def __post_init__(self):
        # Generate safe filename and ID if not provided
        safe_title = re.sub(r'[^a-z0-9]+', '_', self.title.lower()).strip('_')
        if not self.id:
            self.id = f"chapter_{self.order:03d}_{safe_title}"
        if not self.file_name:
            self.file_name = f"{self.id}.xhtml"


