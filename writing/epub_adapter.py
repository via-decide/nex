# Core EPUB Builder
# =============================================================================
class EpubBuilder:
    """
    Constructs an EPUB3 file from metadata and chapters.
    Ensures Kindle (KF8) compatibility by generating both EPUB3 nav and EPUB2 NCX.
    """

    def __init__(self, metadata: EpubMetadata):
        self.metadata = metadata
        self.metadata.validate()
        self.chapters: List[EpubChapter] = []
        self.images: Dict[str, bytes] = {}  # filename -> bytes
        self._manifest_items: List[str] = []
        self._spine_items: List[str] = []
        
        if not MARKDOWN_AVAILABLE:
            logger.warning(
                "The 'markdown' package is not installed. "
                "Markdown conversion will use a rudimentary fallback. "
                "Please run `pip install markdown` for production use."
            )

    def add_chapter(self, title: str, markdown_content: str, order: Optional[int] = None, is_frontmatter: bool = False) -> EpubChapter:
        """
        Parses markdown content, converts to XHTML, and adds it to the EPUB structure.
        """
        if order is None:
            order = len(self.chapters) + 1

        html_content = self._convert_markdown_to_html(markdown_content)
        
        # Wrap in XHTML template
        chapter = EpubChapter(
            title=title,
            content_markdown=markdown_content,
            order=order,
            is_frontmatter=is_frontmatter
        )
        
        xhtml = CHAPTER_XHTML_TEMPLATE.format(
            title=self._escape_xml(chapter.title),
            chapter_id=chapter.id,
            content=html_content
        )
        chapter.html_content = xhtml
        
        self.chapters.append(chapter)
        logger.debug(f"Added chapter: {title} (Order: {order})")
        return chapter

    def add_image(self, file_path: Union[str, Path], filename_in_epub: Optional[str] = None) -> str:
        """
        Adds an image to the EPUB archive.
        Returns the internal path (e.g., 'Images/pic.jpg').
        """
        path = Path(file_path)
        if not path.exists():
            raise FileNotFoundError(f"Image not found: {file_path}")
            
        if not filename_in_epub:
            filename_in_epub = path.name
            
        with open(path, 'rb') as f:
            self.images[filename_in_epub] = f.read()
            
        logger.debug(f"Added image: {filename_in_epub}")
        return f"Images/{filename_in_epub}"

    def set_cover(self, file_path: Union[str, Path]) -> None:
        """Sets the cover image for the EPUB."""
        self.metadata.cover_image_path = Path(file_path)

    def _convert_markdown_to_html(self, text: str) -> str:
        """
        Converts Markdown to strict XHTML compliant with EPUB standards.
        Uses the 'markdown' library if available, otherwise falls back to a basic parser.
        """
        if MARKDOWN_AVAILABLE:
            # Use extensions common in research reports
            extensions = [
                'extra',       # tables, footnotes, def lists, etc.
                'sane_lists',  # better list handling
                'toc',         # generate anchor tags
                'nl2br'        # newlines to <br>
            ]
            html = markdown.markdown(text, extensions=extensions)
            # Ensure empty tags are closed properly for strict XML/XHTML
            html = re.sub(r'<br>', '<br/>', html)
            html = re.sub(r'<hr>', '<hr/>', html)
            html = re.sub(r'<img([^>]+)(?<!/)>', r'<img\1/>', html)
            return html
        else:
            return self._fallback_markdown_parser(text)

    def _fallback_markdown_parser(self, text: str) -> str:
        """
        A highly rudimentary fallback parser if the markdown package is missing.
        DO NOT use this in production for complex research reports.
        """
        html_lines = []
        for line in text.split('\n'):
            line = line.strip()
            if not line:
                continue
            if line.startswith('# '):
                html_lines.append(f"<h1>{self._escape_xml(line[2:])}</h1>")
            elif line.startswith('## '):
                html_lines.append(f"<h2>{self._escape_xml(line[3:])}</h2>")
            elif line.startswith('### '):
                html_lines.append(f"<h3>{self._escape_xml(line[4:])}</h3>")
            elif line.startswith('- '):
                html_lines.append(f"<ul><li>{self._escape_xml(line[2:])}</li></ul>")
            else:
                # Basic bold/italic
                line = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', line)
                line = re.sub(r'\*(.*?)\*', r'<em>\1</em>', line)
                html_lines.append(f"<p>{self._escape_xml(line)}</p>")
        
        # Cleanup adjacent lists
        joined = "\n".join(html_lines)
        joined = joined.replace("</ul>\n<ul>", "\n")
        return joined

    def _escape_xml(self, text: str) -> str:
        """Escapes characters for XML/XHTML compliance."""
        text = str(text)
        text = text.replace("&", "&amp;")
        text = text.replace("<", "&lt;")
        text = text.replace(">", "&gt;")
        text = text.replace('"', "&quot;")
        text = text.replace("'", "&apos;")
        return text

    def _build_manifest_and_spine(self) -> Tuple[str, str, str, str]:
        """
        Builds the XML strings for the manifest, spine, NCX nav points, and XHTML nav items.
        """
        # Sort chapters by order
        self.chapters.sort(key=lambda c: c.order)
        
        manifest = []
        spine = []
        ncx_nav = []
        xhtml_nav = []
        
        # Add Cover Image to manifest if exists
        cover_meta = ""
        guide_cover = ""
        if self.metadata.cover_image_path:
            cover_path = Path(self.metadata.cover_image_path)
            cover_ext = cover_path.suffix.lower()
            mime_type = "image/jpeg" if cover_ext in ['.jpg', '.jpeg'] else "image/png"
            
            # Read cover image
            with open(cover_path, 'rb') as f:
                self.images['cover' + cover_ext] = f.read()
                
            manifest.append(f'<item id="cover-image" href="Images/cover{cover_ext}" media-type="{mime_type}" properties="cover-image"/>')
            cover_meta = '<meta name="cover" content="cover-image"/>'
            
            # Create a cover.xhtml
            cover_xhtml = f"""<?xml version="1.0" encoding="UTF-8"?>
            <html xmlns="http://www.w3.org/1999/xhtml" xmlns:epub="http://www.idpf.org/2007/ops">
            <head><title>Cover</title><style type="text/css">body {{ margin: 0; padding: 0; text-align: center; }} img {{ max-width: 100%; height: auto; }}</style></head>
            <body><img src="Images/cover{cover_ext}" alt="Cover"/></body>
            </html>"""
            
            self.images['cover.xhtml'] = cover_xhtml.encode('utf-8')
            manifest.append('<item id="cover-xhtml" href="Text/cover.xhtml" media-type="application/xhtml+xml"/>')
            spine.append('<itemref idref="cover-xhtml" linear="yes"/>')
            guide_cover = '<reference type="cover" title="Cover" href="Text/cover.xhtml"/>'

        # Add Chapters
        for i, chapter in enumerate(self.chapters, 1):
            manifest.append(f'<item id="{chapter.id}" href="Text/{chapter.file_name}" media-type="application/xhtml+xml"/>')
            spine.append(f'<itemref idref="{chapter.id}" linear="yes"/>')
            
            # NCX Nav Point (EPUB 2)
            ncx_nav.append(f"""
            <navPoint id="navPoint-{i}" playOrder="{i}">
                <navLabel><text>{self._escape_xml(chapter.title)}</text></navLabel>
                <content src="Text/{chapter.file_name}"/>
            </navPoint>""")
            
            # XHTML Nav Item (EPUB 3)
            xhtml_nav.append(f'<li><a href="Text/{chapter.file_name}">{self._escape_xml(chapter.title)}</a></li>')

        # Add arbitrary images to manifest
        for img_name in self.images.keys():
            if img_name.startswith('cover'): continue # handled above
            if img_name.endswith('.xhtml'): continue
            
            mime_type, _ = mimetypes.guess_type(img_name)
            if not mime_type:
                mime_type = "application/octet-stream"
            img_id = re.sub(r'[^a-zA-Z0-9]', '_', img_name)
            manifest.append(f'<item id="img_{img_id}" href="Images/{img_name}" media-type="{mime_type}"/>')

        return (
            "\n        ".join(manifest),
            "\n        ".join(spine),
            "\n        ".join(ncx_nav),
            "\n            ".join(xhtml_nav),
            cover_meta,
            guide_cover
        )

    def build_to_buffer(self) -> BytesIO:
        """
        Compiles the EPUB into an in-memory BytesIO buffer.
        """
        logger.info(f"Building EPUB: {self.metadata.title}")
        buffer = BytesIO()
        
        # EPUB requires specific ZIP formatting (mimetype must be first, uncompressed)
        with zipfile.ZipFile(buffer, 'w') as epub_zip:
            # 1. mimetype
            epub_zip.writestr('mimetype', 'application/epub+zip', compress_type=zipfile.ZIP_STORED)
            
            # 2. META-INF/container.xml
            epub_zip.writestr('META-INF/container.xml', CONTAINER_XML, compress_type=zipfile.ZIP_DEFLATED)
            
            # 3. Compile dynamic contents
            manifest_items, spine_items, ncx_nav, xhtml_nav, cover_meta, guide_cover = self._build_manifest_and_spine()
            
            # Subjects metadata
            subjects_xml = "\n        ".join([f"<dc:subject>{self._escape_xml(s)}</dc:subject>" for s in self.metadata.subjects])
            
            # OEBPS/content.opf
            content_opf = CONTENT_OPF_TEMPLATE.format(
                title=self._escape_xml(self.metadata.title),
                author=self._escape_xml(self.metadata.author),
                language=self.metadata.language,
                identifier=self._escape_xml(self.metadata.identifier),
                publisher=self._escape_xml(self.metadata.publisher),
                description=self._escape_xml(self.metadata.description),
                date=self.metadata.publication_date,
                rights=self._escape_xml(self.metadata.rights),
                subjects_xml=subjects_xml,
                cover_meta=cover_meta,
                manifest_items=manifest_items,
                spine_items=spine_items,
                guide_cover=guide_cover
            )
            epub_zip.writestr('OEBPS/content.opf', content_opf, compress_type=zipfile.ZIP_DEFLATED)
            
            # OEBPS/toc.ncx
            toc_ncx = TOC_NCX_TEMPLATE.format(
                identifier=self._escape_xml(self.metadata.identifier),
                title=self._escape_xml(self.metadata.title),
                nav_points=ncx_nav
            )
            epub_zip.writestr('OEBPS/toc.ncx', toc_ncx, compress_type=zipfile.ZIP_DEFLATED)
            
            # OEBPS/nav.xhtml
            nav_xhtml = NAV_XHTML_TEMPLATE.format(nav_items=xhtml_nav)
            epub_zip.writestr('OEBPS/nav.xhtml', nav_xhtml, compress_type=zipfile.ZIP_DEFLATED)
            
            # OEBPS/Styles/style.css
            epub_zip.writestr('OEBPS/Styles/style.css', CSS_TEMPLATE, compress_type=zipfile.ZIP_DEFLATED)
            
            # Write Chapters
            for chapter in self.chapters:
                epub_zip.writestr(f'OEBPS/Text/{chapter.file_name}', chapter.html_content, compress_type=zipfile.ZIP_DEFLATED)
                
            # Write Images & Cover XHTML
            for img_name, img_data in self.images.items():
                if img_name.endswith('.xhtml'):
                    epub_zip.writestr(f'OEBPS/Text/{img_name}', img_data, compress_type=zipfile.ZIP_DEFLATED)
                else:
                    epub_zip.writestr(f'OEBPS/Images/{img_name}', img_data, compress_type=zipfile.ZIP_DEFLATED)

        buffer.seek(0)
        logger.info("EPUB build complete.")
        return buffer

    def build_to_file(self, output_path: Union[str, Path]) -> None:
        """
        Compiles the EPUB and writes it directly to the filesystem.
        """
        buffer = self.build_to_buffer()
        path = Path(output_path)
        
        # Ensure parent directories exist
        path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(path, 'wb') as f:
            f.write(buffer.read())
            
        logger.info(f"EPUB successfully saved to: {path.absolute()}")


