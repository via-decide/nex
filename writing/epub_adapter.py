class NexReportEpubAdapter:
    """
    Adapter designed specifically for the Nex Autonomous Research Engine.
    Converts Nex's structured research reports (JSON/Dict) into production-ready EPUBs.
    """

    def __init__(self, report_data: Dict[str, Any]):
        """
        Initializes the adapter with Nex report data.
        Expected schema roughly maps to:
        {
            "metadata": {
                "query": "The original research question",
                "author": "Nex Agent",
                "date": "2023-10-25",
                "tags": ["AI", "Research"]
            },
            "executive_summary": "Markdown text...",
            "sections": [
                {"title": "Introduction", "content": "Markdown text..."},
                {"title": "Methodology", "content": "Markdown text..."}
            ],
            "knowledge_graph_summary": "Markdown text...",
            "references": [
                {"id": 1, "url": "...", "title": "...", "snippet": "..."}
            ]
        }
        """
        self.report_data = report_data
        self._validate_report_data()

    def _validate_report_data(self) -> None:
        """Ensures the injected data conforms to expected Nex structures."""
        if not isinstance(self.report_data, dict):
            raise ValueError("Report data must be a dictionary.")
        if "metadata" not in self.report_data:
            logger.warning("No 'metadata' found in report. Using defaults.")
        if "sections" not in self.report_data:
            raise ValueError("Report data must contain a 'sections' list.")

    def _extract_metadata(self) -> EpubMetadata:
        """Maps Nex report metadata to EPUB metadata."""
        meta_node = self.report_data.get("metadata", {})
        
        title = meta_node.get("title", meta_node.get("query", "Nex Autonomous Research Report"))
        author = meta_node.get("author", "Nex Deep Research Engine")
        description = meta_node.get("summary", f"An autonomous deep research report investigating: {title}")
        subjects = meta_node.get("tags", ["Research", "Autonomous Synthesis"])
        
        return EpubMetadata(
            title=title,
            author=author,
            description=description,
            subjects=subjects,
            publisher="Nex Systems"
        )

    def _format_references(self) -> str:
        """Converts the structured references into a Markdown bibliography."""
        refs = self.report_data.get("references", [])
        if not refs:
            return ""
            
        md = ["## Bibliography & Verified Sources\n"]
        md.append("This research report was synthesized by autonomously querying, extracting, and verifying the following open-access sources.\n")
        
        for ref in refs:
            ref_id = ref.get("id", "*")
            title = ref.get("title", "Untitled Source")
            url = ref.get("url", "#")
            snippet = ref.get("snippet", "")
            
            md.append(f"### [{ref_id}] {title}")
            md.append(f"**Source**: [{url}]({url})")
            if snippet:
                md.append(f"> {snippet}")
            md.append("\n---\n")
            
        return "\n".join(md)

    def generate_epub(self, output_path: Union[str, Path], cover_image_path: Optional[str] = None) -> None:
        """
        Orchestrates the conversion from Nex structured data to a final EPUB file.
        """
        metadata = self._extract_metadata()
        if cover_image_path:
            metadata.cover_image_path = Path(cover_image_path)
            
        builder = EpubBuilder(metadata)
        
        order_counter = 1
        
        # 1. Title Page (Auto-generated)
        title_page_md = f"""
<div class="title-page">
<h1>{metadata.title}</h1>
<p class="author">Synthesized by {metadata.author}</p>
<p>Generated on: {datetime.datetime.now().strftime('%Y-%m-%d')}</p>
</div>
"""
        builder.add_chapter("Title Page", title_page_md, order=order_counter, is_frontmatter=True)
        order_counter += 1

        # 2. Executive Summary
        exec_summary = self.report_data.get("executive_summary")
        if exec_summary:
            builder.add_chapter("Executive Summary", f"# Executive Summary\n\n{exec_summary}", order=order_counter)
            order_counter += 1

        # 3. Main Sections
        for section in self.report_data.get("sections", []):
            title = section.get("title", f"Section {order_counter}")
            content = section.get("content", "")
            
            # Ensure the content has a top-level header if missing
            if not content.strip().startswith('#'):
                content = f"# {title}\n\n{content}"
                
            builder.add_chapter(title, content, order=order_counter)
            order_counter += 1

        # 4. Knowledge Graph Synthesis (Optional)
        kg_summary = self.report_data.get("knowledge_graph_summary")
        if kg_summary:
            builder.add_chapter("Knowledge Graph Synthesis", f"# Knowledge Graph Synthesis\n\n{kg_summary}", order=order_counter)
            order_counter += 1

        # 5. References / Bibliography
        references_md = self._format_references()
        if references_md:
            builder.add_chapter("References", references_md, order=order_counter)

        # Build and export
        builder.build_to_file(output_path)


